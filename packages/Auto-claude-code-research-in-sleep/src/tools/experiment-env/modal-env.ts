import fs from "fs";
import { EnvBackend, EnvError, runShell, shellQuote } from "./env-backend.js";

const DEFAULT_TIMEOUT = 21600;

export class ModalEnv extends EnvBackend {
  private _appName(runSpec?: Record<string, unknown>): string {
    if (runSpec?.exp_name) return runSpec.exp_name as string;
    return "aris-experiment";
  }

  private _secretsList(): string[] {
    const s = this.config.modal_secrets;
    if (!s) return [];
    if (typeof s === "string") return [s];
    return [...(s as string[])];
  }

  provision(): Record<string, unknown> {
    if (this.dryRun) return this._announce("provision", "modal token current");
    const { returncode: rc } = runShell("modal token current");
    if (rc !== 0) {
      throw new EnvError(10, "modal not authenticated; run `modal setup`");
    }
    return { status: "ready", env_type: "modal", modal_app: this._appName() };
  }

  preflight(): Record<string, unknown> {
    const checks: Record<string, unknown>[] = [
      { name: "modal-cli", ok: true, detail: "modal present" },
    ];
    if (!this.config.modal_volume) {
      checks.push({
        name: "volume",
        ok: false,
        detail: "modal_volume not set",
      });
    }
    return {
      ok: checks.every((c) => c.ok),
      checks,
      conda_resolved: null,
    };
  }

  sync(src: string): Record<string, unknown> {
    return {
      status: "synced",
      method: "modal_mount",
      remote_path: "/workspace",
      local_src: src,
    };
  }

  private _generateLauncher(
    runSpec: Record<string, unknown>,
    launcherPath: string,
    writeFile = true,
  ): string {
    const appName = this._appName(runSpec);
    const script = runSpec.script as string;
    const args = (runSpec.args as string[]) || [];
    const gpu = (this.config.modal_gpu as string) || "auto";
    const timeout = (this.config.modal_timeout as number) || DEFAULT_TIMEOUT;
    const volume = (this.config.modal_volume as string) || "experiment-results";
    const secrets = this._secretsList();
    const pipPkgs = (this.config.pip_install as string[]) || [
      "torch",
      "transformers",
      "accelerate",
      "datasets",
      "wandb",
    ];

    const pipStr = pipPkgs.map((p) => JSON.stringify(p)).join(", ");
    const argsStr = args.map((a) => JSON.stringify(a)).join(", ");
    const scriptLit = JSON.stringify(script);
    const appLit = JSON.stringify(appName);
    const gpuLit = JSON.stringify(gpu);
    const volumeLit = JSON.stringify(volume);
    const secretsStr = secrets
      .map((s) => `modal.Secret.from_name(${JSON.stringify(s)})`)
      .join(", ");
    const secretsLine = secrets.length ? `        secrets=[${secretsStr}],\n` : "";
    const argsTail = argsStr ? ", " + argsStr : "";

    const code = `\
"""Auto-generated by experiment_env/modal_env.py (Pattern A).
Do not edit by hand — regenerate via \`env_helper.py deploy\`."""
import subprocess
import modal

app = modal.App(${appLit})
image = modal.Image.debian_slim(python_version="3.11").pip_install(
    ${pipStr}
)

local_code = modal.Mount.from_local_dir(".", remote_path="/workspace")
volume = modal.Volume.from_name(${volumeLit}, create_if_missing=True)


@app.function(
    image=image,
    gpu=${gpuLit},
    mounts=[local_code],
    volumes={"/results": volume},
    timeout=${Math.floor(timeout)},
${secretsLine})\ndef train():
    subprocess.run(
        ["python", ${scriptLit}${argsTail}],
        cwd="/workspace",
        check=True,
    )
    volume.commit()


@app.local_entrypoint()
def main():
    train.remote()
    print("Training complete. Results saved to Modal volume "
          + ${JSON.stringify(volume + ".")})
`;

    if (writeFile) {
      fs.writeFileSync(launcherPath, code);
    }
    return code;
  }

  deploy(runSpec: Record<string, unknown>): Record<string, unknown> {
    const appName = this._appName(runSpec);
    const launcherPath = (runSpec.launcher_path as string) || `modal_launcher_${appName}.py`;
    const code = this._generateLauncher(runSpec, launcherPath, !this.dryRun);

    if (this.dryRun) {
      return {
        status: "dry_run",
        action: "deploy",
        launcher_path: launcherPath,
        launcher: code,
        command: `modal run ${launcherPath}`,
      };
    }

    const { stdout: out, returncode: rc } = runShell(`modal run ${shellQuote(launcherPath)}`);
    if (rc !== 0) throw new EnvError(13, `modal run failed: ${out}`);

    const handle = {
      type: "modal_app",
      app: appName,
      session_name: appName,
      log_file: null,
      volume: this.config.modal_volume,
    };
    return {
      status: "launched",
      handle,
      log_file: launcherPath,
      launcher_path: launcherPath,
    };
  }

  monitor(handle: Record<string, unknown>): Record<string, unknown> {
    const app = handle.app as string;
    const listCmd = "modal app list";
    if (this.dryRun) return this._announce("monitor", listCmd);

    const { stdout: out } = runShell(listCmd);
    const running = out.includes(app);
    let tail = "";
    if (running) {
      const { stdout: t } = runShell(`modal app logs ${shellQuote(app)}`);
      tail = t.slice(-2000);
    }
    return { status: running ? "running" : "done", exit_code: null, tail };
  }

  collectResults(): Record<string, unknown> {
    const volume = this.config.modal_volume as string | undefined;
    if (!volume) throw new EnvError(15, "no modal_volume configured");

    if (this.dryRun) {
      const cmds = [
        `modal volume ls ${shellQuote(volume)}`,
        `modal volume get ${shellQuote(volume)} /results/ ./results/`,
      ];
      return this._announce("collect", cmds.join(" && "));
    }

    const { stdout: out } = runShell(`modal volume ls ${shellQuote(volume)}`);
    const results = out.trim().split("\n");
    fs.mkdirSync("./results/", { recursive: true });
    const { stdout: getOut, returncode: getRc } = runShell(
      `modal volume get ${shellQuote(volume)} /results/ ./results/`,
    );
    if (getRc !== 0) {
      throw new EnvError(15, `modal volume get failed: ${getOut}`);
    }
    return {
      status: "collected",
      results,
      local_copy: "./results/",
      volume,
    };
  }

  destroy(): Record<string, unknown> {
    const app = (this.config.modal_app_file as string) || "aris-experiment";
    const volume = this.config.modal_volume as string | undefined;

    if (this.dryRun) {
      const parts = [`modal app stop ${app}`];
      if (volume) parts.push(`modal volume rm ${volume}`);
      return this._announce("destroy", parts.join(" && "));
    }

    runShell(`modal app stop ${shellQuote(app)}`);
    const stopped = true;
    let removed = false;
    if (volume) {
      runShell(`modal volume rm ${shellQuote(volume)}`);
      removed = true;
    }
    return {
      status: "destroyed",
      app_stopped: stopped,
      volume_removed: removed,
      note: "modal is serverless — no instance to destroy",
    };
  }
}
