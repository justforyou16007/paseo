"""modal_env.py — ModalEnv backend (gpu: modal).

Absorbs the serverless-modal skill's launcher generation, run, monitor,
collect, cleanup. Deploys by GENERATING a modal_launcher.py that wraps
the training script, then `modal run`. No SSH, no conda, no screen.

Commands reproduce:
- skills/serverless-modal/SKILL.md Pattern A launcher (101-145)
- run (231-236), verify (238-243), collect via volume (245-259),
  cleanup (261-268, no instance destroy — serverless scale-to-zero)

The generated launcher faithfully reproduces Pattern A:
  modal.Image.debian_slim().pip_install(...) +
  modal.Mount.from_local_dir(".", "/workspace") +
  modal.Volume.from_name(<vol>, create_if_missing=True) +
  @app.function(image, gpu, timeout, mounts, volumes={"/results": vol},
                secrets=[modal.Secret.from_name(s) for s in modal_secrets]) +
  volume.commit() + @app.local_entrypoint train.remote()

ALL of secrets/volume/timeout/gpu/image are read from config so no field
is dropped on migration.
"""

import shlex
import textwrap
from pathlib import Path

try:
    from .env_backend import EnvBackend, EnvError, run
except ImportError:  # script mode
    from env_backend import EnvBackend, EnvError, run

DEFAULT_TIMEOUT = 21600


class ModalEnv(EnvBackend):
    """Modal serverless; one app per deploy."""

    def _app_name(self, run_spec=None):
        if run_spec and run_spec.get("exp_name"):
            return run_spec["exp_name"]
        return "aris-experiment"

    def _secrets_list(self):
        s = self.config.get("modal_secrets") or []
        if isinstance(s, str):
            return [s]
        return list(s)

    def provision(self) -> dict:
        if self.dry_run:
            return self._announce("provision", "modal token current")
        _, rc = run("modal token current")
        if rc != 0:
            raise EnvError(10, "modal not authenticated; run `modal setup`")
        return {"status": "ready", "env_type": "modal",
                "modal_app": self._app_name()}

    def preflight(self) -> dict:
        # cost estimate is advisory; the SKILL presents it. Here we just
        # confirm modal is callable and the config is complete.
        checks = [{"name": "modal-cli", "ok": True, "detail": "modal present"}]
        if not self.config.get("modal_volume"):
            checks.append({"name": "volume", "ok": False,
                           "detail": "modal_volume not set"})
        return {"ok": all(c["ok"] for c in checks), "checks": checks,
                "conda_resolved": None}

    def sync(self, src: str) -> dict:
        # Modal mounts local code at run time; no pre-sync.
        return {"status": "synced", "method": "modal_mount",
                "remote_path": "/workspace", "local_src": src}

    def _generate_launcher(self, run_spec, launcher_path, write_file=True):
        app_name = self._app_name(run_spec)
        script = run_spec["script"]
        args = run_spec.get("args", [])
        gpu = self.config.get("modal_gpu", "auto")
        timeout = self.config.get("modal_timeout", DEFAULT_TIMEOUT)
        volume = self.config.get("modal_volume", "experiment-results")
        secrets = self._secrets_list()
        # pip packages: sensible default; allow override via config.pip_install
        pip_pkgs = self.config.get("pip_install",
                                   ["torch", "transformers", "accelerate",
                                    "datasets", "wandb"])
        # Use json.dumps to emit valid PYTHON string literals (shlex.quote
        # would produce shell-safe strings, not Python literals — bare
        # identifiers like A100-80GB would be a syntax error).
        import json as _json
        pip_str = ", ".join(_json.dumps(p) for p in pip_pkgs)
        args_str = ", ".join(_json.dumps(a) for a in args)
        script_lit = _json.dumps(script)
        app_lit = _json.dumps(app_name)
        gpu_lit = _json.dumps(gpu)
        volume_lit = _json.dumps(volume)
        secrets_str = ", ".join(
            f"modal.Secret.from_name({_json.dumps(s)})" for s in secrets)
        secrets_line = f"        secrets=[{secrets_str}],\n" if secrets else ""
        args_tail = (", " + args_str) if args_str else ""
        code = textwrap.dedent(f'''\
            """Auto-generated by experiment_env/modal_env.py (Pattern A).
            Do not edit by hand — regenerate via `env_helper.py deploy`."""
            import subprocess
            import modal

            app = modal.App({app_lit})
            image = modal.Image.debian_slim(python_version="3.11").pip_install(
                {pip_str}
            )

            local_code = modal.Mount.from_local_dir(".", remote_path="/workspace")
            volume = modal.Volume.from_name({volume_lit}, create_if_missing=True)


            @app.function(
                image=image,
                gpu={gpu_lit},
                mounts=[local_code],
                volumes={{"/results": volume}},
                timeout={int(timeout)},
            {secrets_line}            )
            def train():
                subprocess.run(
                    ["python", {script_lit}{args_tail}],
                    cwd="/workspace",
                    check=True,
                )
                volume.commit()


            @app.local_entrypoint()
            def main():
                train.remote()
                print("Training complete. Results saved to Modal volume "
                      + {_json.dumps(volume + ".")})
            ''')
        if write_file:
            Path(launcher_path).write_text(code)
        return code

    def deploy(self, run_spec: dict) -> dict:
        app_name = self._app_name(run_spec)
        launcher_path = run_spec.get("launcher_path",
                                     f"modal_launcher_{app_name}.py")
        code = self._generate_launcher(run_spec, launcher_path, write_file=not self.dry_run)
        if self.dry_run:
            return {"status": "dry_run", "action": "deploy",
                    "launcher_path": launcher_path,
                    "launcher": code,
                    "command": f"modal run {launcher_path}"}
        out, rc = run(f"modal run {shlex.quote(launcher_path)}")
        if rc != 0:
            raise EnvError(13, f"modal run failed: {out}")
        handle = {"type": "modal_app", "app": app_name,
                  "session_name": app_name, "log_file": None,
                  "volume": self.config.get("modal_volume")}
        return {"status": "launched", "handle": handle,
                "log_file": launcher_path, "launcher_path": launcher_path}

    def monitor(self, handle: dict) -> dict:
        app = handle["app"]
        list_cmd = "modal app list"
        if self.dry_run:
            return self._announce("monitor", list_cmd)
        out, _ = run(list_cmd)
        running = app in out
        tail = ""
        if running:
            t, _ = run(f"modal app logs {shlex.quote(app)}")
            tail = t[-2000:]
        return {"status": "running" if running else "done",
                "exit_code": None, "tail": tail}

    def collect_results(self) -> dict:
        volume = self.config.get("modal_volume")
        if not volume:
            raise EnvError(15, "no modal_volume configured")
        if self.dry_run:
            cmds = [f"modal volume ls {shlex.quote(volume)}",
                    f"modal volume get {shlex.quote(volume)} /results/ ./results/"]
            return self._announce("collect", " && ".join(cmds))
        out, _ = run(f"modal volume ls {shlex.quote(volume)}")
        results = out.strip().splitlines()
        Path("./results/").mkdir(parents=True, exist_ok=True)
        get_out, get_rc = run(
            f"modal volume get {shlex.quote(volume)} /results/ ./results/")
        if get_rc != 0:
            raise EnvError(15, f"modal volume get failed: {get_out}")
        return {"status": "collected", "results": results,
                "local_copy": "./results/", "volume": volume}

    def destroy(self) -> dict:
        app = self.config.get("modal_app_file") or "aris-experiment"
        volume = self.config.get("modal_volume")
        if self.dry_run:
            parts = [f"modal app stop {app}"]
            if volume:
                parts.append(f"modal volume rm {volume}")
            return self._announce("destroy", " && ".join(parts))
        stopped = False
        run(f"modal app stop {shlex.quote(app)}", check=False)
        stopped = True
        removed = False
        if volume:
            run(f"modal volume rm {shlex.quote(volume)}", check=False)
            removed = True
        return {"status": "destroyed", "app_stopped": stopped,
                "volume_removed": removed,
                "note": "modal is serverless — no instance to destroy"}
